#include<bits/stdc++.h>'
using namespace std;
int main()
{
    long long t,n,m,a,b;
   // double i,n;
    cin>>t;
    while(t--){
        cin>>a>>b>>n>>m;
        //cout<<fixed<<setprecision(0)<<ceil(n/2)<<endl;
        if(a>=n&&b>=m){
            cout<<"YES"<<endl;
        }
        if(m==0||n==0){
            cout<<"NO"<<endl;
        }
        else {
            cout<<"NO"<<endl;
        }
    }
}
